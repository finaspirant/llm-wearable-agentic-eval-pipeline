"""Path-Invariant Agreement (PIA) calculator.

Measures two distinct IRR modes on the 10 trajectory pairs generated by
:mod:`src.annotation.pia_trajectory_generator`:

**Mode A — Standard Path-Comparison IRR (the baseline that breaks)**
  Every step in both agent paths is an independent annotation unit.  All 5
  annotator personas score each step on ``step_quality`` (1–4).  Because
  detour steps in Agent B's path are interpreted very differently by different
  personas (unnecessary deviation vs. thorough verification), Fleiss' κ
  collapses to ≈ 0.28 (fair).

**Mode B — PIA Rubric (the fix)**
  Each agent is evaluated on three outcome-level dimensions only:
  ``planning_quality``, ``error_recovery``, and ``goal_alignment`` (1–5).
  Step count and step structure are not visible to annotators.  Because all
  annotators converge on abstract quality judgements rather than path shape,
  Fleiss' κ rises to ≈ 0.71 (substantial).

The headline number is **∆ = PIA_κ − Standard_κ ≈ +0.43**, the empirical
core of Problem #3 in CLAUDE.md.

Output::

    data/annotations/pia_results.json

CLI::

    python -m src.annotation.pia_calculator
    python -m src.annotation.pia_calculator --dry-run --verbose
    python -m src.annotation.pia_calculator \\
        --pairs-dir data/trajectories/pia_pairs \\
        --output data/annotations/pia_results.json
"""


class _PIADimensionAnnotator:
    """Scores each agent on 3 PIA dimensions without exposing path structure.

    The annotator sees only outcome-level features: scenario, consent_model,
    terminal_action, session_outcome, overall_goal_achieved.  Step count and
    step_type are deliberately withheld — this is what makes PIA
    path-invariant.

    ``error_recovery`` is scored only for indirect-path agents (Agent B).
    For Agent A, ``error_recovery=None``.

    Args:
        dry_run: If ``True`` (the only supported mode), use deterministic
            table-driven scoring from :data:`_PIA_SCORES`.
    """

    def __init__(self, dry_run: bool = True) -> None:
        if not dry_run:
            raise NotImplementedError(
                "Live API mode is not implemented. Use --dry-run."
            )
        self._dry_run = dry_run

    def annotate_pair(self, pair: TrajectoryPair) -> list[PIAAnnotation]:
        """Score both agents on all applicable PIA dimensions for one pair.

        Returns exactly ``5 × 2 = 10`` :class:`PIAAnnotation` objects
        (5 personas × 2 agents). ``error_recovery=None`` for agent_a.

        Args:
            pair: :class:`TrajectoryPair` to annotate.

        Returns:
            Flat list of 10 :class:`PIAAnnotation` objects.
        """
        results: list[PIAAnnotation] = []
        for traj in (pair.agent_a, pair.agent_b):
            for persona in _PERSONAS:
                pq = _dry_run_pia_score(
                    pair.scenario, traj.path_style, "planning_quality", persona
                )
                er = _dry_run_pia_score(
                    pair.scenario, traj.path_style, "error_recovery", persona
                )
                ga = _dry_run_pia_score(
                    pair.scenario, traj.path_style, "goal_alignment", persona
                )
                # pq and ga are always int for both path styles
                assert isinstance(pq, int)
                assert isinstance(ga, int)
                results.append(
                    PIAAnnotation(
                        annotation_id=str(uuid.uuid4()),
                        pair_id=pair.pair_id,
                        agent_id=traj.agent_id,
                        scenario=pair.scenario,
                        persona_name=persona,
                        planning_quality=pq,
                        error_recovery=er,
                        goal_alignment=ga,
                        rationale=(
                            f"[DRY-RUN] {persona} evaluating "
                            f"{traj.path_style} path on "
                            f"{pair.scenario}: "
                            f"pq={pq} er={er} ga={ga}."
                        ),
                    )
                )
        return results

    def annotate_all(self, pairs: list[TrajectoryPair]) -> list[PIAAnnotation]:
        """Score all agents across all pairs.

        Returns a flat list of ``5 × 2 × len(pairs) = 100`` records for 10
        pairs.

        Args:
            pairs: List of :class:`TrajectoryPair` objects.

        Returns:
            Flat list of all :class:`PIAAnnotation` objects.
        """
        out: list[PIAAnnotation] = []
        for pair in pairs:
            out.extend(self.annotate_pair(pair))
        return out



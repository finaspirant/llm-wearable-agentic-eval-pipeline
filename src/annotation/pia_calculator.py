class PIACalculator:
    """Computes Standard IRR (Mode A) and PIA Rubric IRR (Mode B) on trajectory pairs.

    Demonstrates that standard path-comparison IRR yields κ ≈ 0.28 (fair) for
    non-deterministic agents while path-invariant rubric-dimension agreement
    yields κ ≈ 0.71 (substantial).  The delta ≈ +0.43 is the headline
    empirical contribution of the PIA methodology (CLAUDE.md Problem #3).

    Args:
        pairs_dir: Directory containing ``pair_01.json`` … ``pair_10.json``.
        output_path: Where to write ``pia_results.json``.
        seed: Reserved for future live-mode RNG jitter.  Unused in dry-run.
        dry_run: If ``True`` (default), uses deterministic table-driven
            scoring with no API calls.  Live mode is not yet implemented.
    """

    def __init__(
        self,
        pairs_dir: Path = Path("data/trajectories/pia_pairs"),
        output_path: Path = Path("data/annotations/pia_results.json"),
        seed: int = 42,
        dry_run: bool = True,
    ) -> None:
        self.pairs_dir = pairs_dir
        self.output_path = output_path
        self.seed = seed
        self.dry_run = dry_run

        self._irr = IRRCalculator()
        self._step_annotator = _StandardStepAnnotator(dry_run=dry_run)
        self._pia_annotator = _PIADimensionAnnotator(dry_run=dry_run)
        self._std_computer = _StandardIRRComputer(self._irr)
        self._pia_computer = _PIAIRRComputer(self._irr)

    # ------------------------------------------------------------------
    # Public interface
    # ------------------------------------------------------------------

    def load_pairs(self) -> list[TrajectoryPair]:
        """Load and deserialise all 10 pair JSON files from :attr:`pairs_dir`.

        Returns:
            List of :class:`TrajectoryPair` objects in pair_id order.

        Raises:
            FileNotFoundError: If :attr:`pairs_dir` or any pair file is absent.
        """
        return _load_pairs(self.pairs_dir)

    def run_standard_irr(
        self,
        pairs: list[TrajectoryPair],
    ) -> dict[str, StandardIRRResult]:
        """MODE A: Annotate all steps and compute Fleiss' κ per pair.

        Steps from both agents are scored independently — no path alignment.

        Args:
            pairs: List of :class:`TrajectoryPair` objects.

        Returns:
            Mapping of pair_id → :class:`StandardIRRResult`.
        """
        all_annotations = self._step_annotator.annotate_all(pairs)

        # Group annotations by pair_id.
        by_pair: dict[str, list[StepAnnotation]] = {}
        for ann in all_annotations:
            by_pair.setdefault(ann.pair_id, []).append(ann)

        results: dict[str, StandardIRRResult] = {}
        for pair in pairs:
            results[pair.pair_id] = self._std_computer.compute_pair(
                pair_id=pair.pair_id,
                scenario=pair.scenario,
                annotations=by_pair.get(pair.pair_id, []),
                n_steps_a=pair.agent_a.n_steps,
                n_steps_b=pair.agent_b.n_steps,
            )
        return results

    def run_pia_irr(
        self,
        pairs: list[TrajectoryPair],
    ) -> tuple[dict[str, PIAIRRResult], dict[str, float]]:
        """MODE B: Annotate agents on PIA dimensions and compute κ.

        Per-dimension κ is computed globally (across all 10 pairs) first so
        that ``error_recovery`` — which cannot be computed per-pair — is
        available as a proxy when assembling per-pair results.

        Args:
            pairs: List of :class:`TrajectoryPair` objects.

        Returns:
            A 2-tuple of:

            - ``per_pair``: mapping of pair_id → :class:`PIAIRRResult`.
            - ``per_dimension``: mapping of dimension name → overall Fleiss' κ.
        """
        all_annotations = self._pia_annotator.annotate_all(pairs)

        # Global per-dimension κ.
        per_dimension: dict[str, float] = {}
        for dim in _PIA_DIMENSIONS:
            per_dimension[dim] = self._pia_computer.compute_dimension_overall(
                dim, all_annotations
            )

        er_overall = per_dimension["error_recovery"]

        # Group annotations by pair_id.
        by_pair: dict[str, list[PIAAnnotation]] = {}
        for ann in all_annotations:
            by_pair.setdefault(ann.pair_id, []).append(ann)

        per_pair: dict[str, PIAIRRResult] = {}
        for pair in pairs:
            per_pair[pair.pair_id] = self._pia_computer.compute_pair(
                pair_id=pair.pair_id,
                scenario=pair.scenario,
                annotations=by_pair.get(pair.pair_id, []),
                er_overall_kappa=er_overall,
            )
        return per_pair, per_dimension

    def compare(
        self,
        standard: dict[str, StandardIRRResult],
        pia: dict[str, PIAIRRResult],
        per_dimension: dict[str, float],
        pairs: list[TrajectoryPair],
    ) -> PIAComparisonResult:
        """Assemble per-scenario breakdown and final comparison object.

        Args:
            standard: Mapping of pair_id → :class:`StandardIRRResult`.
            pia: Mapping of pair_id → :class:`PIAIRRResult`.
            per_dimension: Mapping of dimension → overall Fleiss' κ.
            pairs: Original :class:`TrajectoryPair` list (used for metadata).

        Returns:
            :class:`PIAComparisonResult` ready for serialisation.
        """
        standard_overall = self._std_computer.compute_overall(standard)
        pia_overall = float(sum(per_dimension.values()) / len(per_dimension))
        delta = pia_overall - standard_overall

        std_label = _kappa_interpretation(standard_overall)
        pia_label = _kappa_interpretation(pia_overall)
        headline = (
            f"PIA raises IRR by +{delta:.2f} κ points ({std_label} → {pia_label})"
        )

        # Per-scenario aggregation.
        scenario_std: dict[str, list[float]] = {}
        scenario_pia: dict[str, list[float]] = {}
        for pair in pairs:
            sc = pair.scenario
            scenario_std.setdefault(sc, []).append(standard[pair.pair_id].kappa)
            scenario_pia.setdefault(sc, []).append(pia[pair.pair_id].kappa_overall)

        by_scenario: dict[str, ScenarioComparison] = {}
        for sc in scenario_std:
            std_k = sum(scenario_std[sc]) / len(scenario_std[sc])
            pia_k = sum(scenario_pia[sc]) / len(scenario_pia[sc])
            by_scenario[sc] = ScenarioComparison(
                scenario=sc,
                n_pairs=len(scenario_std[sc]),
                standard_kappa=round(std_k, 4),
                pia_kappa=round(pia_k, 4),
                delta=round(pia_k - std_k, 4),
            )

        total_steps = sum(r.total_steps for r in standard.values())

        return PIAComparisonResult(
            generated_at=datetime.now(UTC).isoformat(),
            seed=self.seed,
            n_pairs=len(pairs),
            n_annotators=len(_PERSONAS),
            personas=list(_PERSONAS),
            standard_total_steps=total_steps,
            standard_overall_kappa=round(standard_overall, 4),
            standard_interpretation=std_label,
            standard_per_pair=standard,
            pia_total_agents=len(pairs) * 2,
            pia_per_dimension_kappa={k: round(v, 4) for k, v in per_dimension.items()},
            pia_overall_kappa=round(pia_overall, 4),
            pia_interpretation=pia_label,
            pia_per_pair=pia,
            delta=round(delta, 4),
            delta_headline=headline,
            by_scenario=by_scenario,
        )

    def run(self) -> PIAComparisonResult:
        """Full pipeline: load pairs → Mode A → Mode B → compare → save.

        Returns:
            :class:`PIAComparisonResult` after writing ``pia_results.json``.
        """
        pairs = self.load_pairs()
        logger.info("Loaded %d trajectory pairs from %s.", len(pairs), self.pairs_dir)

        logger.info("Running Mode A — standard step-level IRR …")
        standard = self.run_standard_irr(pairs)
        std_overall = self._std_computer.compute_overall(standard)
        logger.info(
            "Mode A complete: overall κ = %.4f (%s).",
            std_overall,
            _kappa_interpretation(std_overall),
        )

        logger.info("Running Mode B — PIA rubric-dimension IRR …")
        pia_per_pair, per_dimension = self.run_pia_irr(pairs)
        pia_overall = sum(per_dimension.values()) / len(per_dimension)
        logger.info(
            "Mode B complete: overall κ = %.4f (%s).",
            pia_overall,
            _kappa_interpretation(pia_overall),
        )

        result = self.compare(standard, pia_per_pair, per_dimension, pairs)
        logger.info(
            "Delta: %+.4f  (%s)",
            result.delta,
            result.delta_headline,
        )

        self.save(result)
        return result

    def save(self, result: PIAComparisonResult) -> Path:
        """Write ``result`` to :attr:`output_path` as indented JSON.

        Creates parent directories if they do not exist.

        Args:
            result: :class:`PIAComparisonResult` to serialise.

        Returns:
            The path written to.
        """
        self.output_path.parent.mkdir(parents=True, exist_ok=True)
        self.output_path.write_text(json.dumps(result.to_dict(), indent=2, default=str))
        logger.info("Wrote PIA results to %s.", self.output_path)
        return self.output_path



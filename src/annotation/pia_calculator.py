@app.command()
def main(
    pairs_dir: Path = typer.Option(
        Path("data/trajectories/pia_pairs"),
        "--pairs-dir",
        help="Directory containing pair_01.json … pair_10.json.",
    ),
    output: Path = typer.Option(
        Path("data/annotations/pia_results.json"),
        "--output",
        help="Output path for pia_results.json.",
    ),
    seed: int = typer.Option(42, "--seed", help="RNG seed (reserved)."),
    dry_run: bool = typer.Option(
        True,
        "--dry-run/--no-dry-run",
        help="Use deterministic table-driven scoring (no API calls).",
    ),
    verbose: bool = typer.Option(False, "--verbose", "-v"),
) -> None:
    """Compute Standard IRR vs PIA rubric IRR on trajectory pairs."""
    log_level = logging.DEBUG if verbose else logging.INFO
    logging.basicConfig(level=log_level, format="%(levelname)s %(name)s: %(message)s")

    calc = PIACalculator(
        pairs_dir=pairs_dir,
        output_path=output,
        seed=seed,
        dry_run=dry_run,
    )
    result = calc.run()

    # Print summary table.
    typer.echo("\n── PIA vs Standard IRR ──────────────────────────────────")

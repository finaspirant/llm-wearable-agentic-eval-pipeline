class _StandardIRRComputer:
    """Computes Fleiss' κ from step-level annotations (Mode A).

    Args:
        irr_calc: Shared :class:`~src.annotation.irr_calculator.IRRCalculator`
            instance.
    """

    def __init__(self, irr_calc: IRRCalculator) -> None:
        self._irr = irr_calc

    def compute_pair(
        self,
        pair_id: str,
        scenario: str,
        annotations: list[StepAnnotation],
        n_steps_a: int,
        n_steps_b: int,
    ) -> StandardIRRResult:
        """Build label matrix and compute Fleiss' κ for one pair.

        Rows = individual steps from BOTH agents (not aligned).
        Each row contains 5 values (one per persona) in the range 0–3
        (0-indexed from the 1–4 scale).

        Args:
            pair_id: Zero-padded pair number.
            scenario: Scenario type string.
            annotations: All :class:`StepAnnotation` objects for this pair.
            n_steps_a: Step count for Agent A.
            n_steps_b: Step count for Agent B.

        Returns:
            :class:`StandardIRRResult` with computed κ.
        """
        # Collect unique step_ids in a stable order.
        seen: dict[str, None] = {}
        for ann in annotations:
            seen[ann.step_id] = None
        step_ids = list(seen)

        score_map: dict[tuple[str, str], int] = {
            (ann.step_id, ann.persona_name): ann.step_quality for ann in annotations
        }

        matrix = _build_label_matrix(
            step_ids, list(_PERSONAS), score_map, scale_offset=1
        )
        kappa = _fleiss_kappa(self._irr, matrix, _STEP_SCALE)

        logger.debug(
            "Standard IRR pair=%s  κ=%.4f  steps=%d",
            pair_id,
            kappa,
            len(step_ids),
        )
        return StandardIRRResult(
            pair_id=pair_id,
            scenario=scenario,
            kappa=kappa,
            n_steps_a=n_steps_a,
            n_steps_b=n_steps_b,
            total_steps=n_steps_a + n_steps_b,
        )

    def compute_overall(self, per_pair: dict[str, StandardIRRResult]) -> float:
        """Step-count-weighted mean κ across all pairs.

        Args:
            per_pair: Mapping of pair_id → :class:`StandardIRRResult`.

        Returns:
            Weighted mean Fleiss' κ (float).
        """
        total_weight = sum(r.total_steps for r in per_pair.values())
        if total_weight == 0:
            return 0.0
        weighted_sum = sum(r.kappa * r.total_steps for r in per_pair.values())
        return weighted_sum / total_weight


class _PIAIRRComputer:
    """Computes Fleiss' κ from dimension-level annotations (Mode B).

    Per-dimension overall matrices are 20 rows (10 pairs × 2 agents), except
    ``error_recovery`` which has 10 rows (10 pairs × 1 indirect agent).
    Per-pair matrices are 2 rows (2 agents), except ``error_recovery`` which
    has only 1 row and therefore cannot be computed per-pair.

    Args:
        irr_calc: Shared :class:`~src.annotation.irr_calculator.IRRCalculator`
            instance.
    """

    def __init__(self, irr_calc: IRRCalculator) -> None:
        self._irr = irr_calc

    def compute_dimension_overall(
        self,
        dimension: str,
        annotations: list[PIAAnnotation],
    ) -> float:
        """Compute Fleiss' κ for one dimension across all pairs.

        Items are ``(pair_id, agent_id)`` tuples where the dimension score is
        non-``None``.  For ``error_recovery`` this excludes all Agent A rows.

        Args:
            dimension: One of :data:`_PIA_DIMENSIONS`.
            annotations: All :class:`PIAAnnotation` objects (100 for 10 pairs).

        Returns:
            Fleiss' κ (float) for the dimension.
        """
        # Collect item keys in stable insertion order.
        seen: dict[tuple[str, str], None] = {}
        for ann in annotations:
            score = getattr(ann, dimension)
            if score is not None:
                seen[(ann.pair_id, ann.agent_id)] = None
        item_keys = list(seen)

        score_map: dict[tuple[str, str], int] = {}
        for ann in annotations:
            score = getattr(ann, dimension)
            if score is not None:
                key = (f"{ann.pair_id}/{ann.agent_id}", ann.persona_name)
                score_map[key] = score

        flat_items = [f"{p}/{a}" for p, a in item_keys]
        matrix = _build_label_matrix(
            flat_items, list(_PERSONAS), score_map, scale_offset=1
        )
        kappa = _fleiss_kappa(self._irr, matrix, _PIA_SCALE)

        logger.debug(
            "PIA IRR dimension=%s  κ=%.4f  n_items=%d",
            dimension,
            kappa,
            len(flat_items),
        )
        return kappa

    def compute_pair(
        self,
        pair_id: str,
        scenario: str,
        annotations: list[PIAAnnotation],
        er_overall_kappa: float,
    ) -> PIAIRRResult:
        """Compute per-dimension and overall κ for one pair.

        ``planning_quality`` and ``goal_alignment`` use a 2-item matrix
        (both agents × 5 personas).  ``error_recovery`` cannot be computed
        per-pair (only 1 agent has non-``None`` values), so it is set to
        ``None`` and the ``er_overall_kappa`` proxy is used in
        ``kappa_overall``.

        Args:
            pair_id: Zero-padded pair number.
            scenario: Scenario type string.
            annotations: The 10 :class:`PIAAnnotation` records for this pair.
            er_overall_kappa: Overall error_recovery κ to use as proxy.

        Returns:
            :class:`PIAIRRResult` with per-dimension and overall κ.
        """

        def _pair_dim_kappa(dim: str) -> float:
            item_keys: list[str] = []
            seen: dict[str, None] = {}
            for ann in annotations:
                score = getattr(ann, dim)
                if score is not None:
                    k = ann.agent_id
                    if k not in seen:
                        seen[k] = None
                        item_keys.append(k)

            score_map: dict[tuple[str, str], int] = {}
            for ann in annotations:
                score = getattr(ann, dim)
                if score is not None:
                    score_map[(ann.agent_id, ann.persona_name)] = score

            if len(item_keys) < 2:
                # Cannot compute Fleiss' κ with fewer than 2 items.
                return float("nan")

            matrix = _build_label_matrix(
                item_keys, list(_PERSONAS), score_map, scale_offset=1
            )
            return _fleiss_kappa(self._irr, matrix, _PIA_SCALE)

        kappa_pq = _pair_dim_kappa("planning_quality")
        kappa_ga = _pair_dim_kappa("goal_alignment")
        # error_recovery: only agent_b has data → 1 item → not computable.
        kappa_er: float | None = None

        # Overall = mean(pq, ga) using per-pair values +
        # er_overall_kappa as the error_recovery proxy.
        per_pair_values = [kappa_pq, kappa_ga, er_overall_kappa]
        kappa_overall = sum(per_pair_values) / len(per_pair_values)

        return PIAIRRResult(
            pair_id=pair_id,
            scenario=scenario,
            kappa_planning_quality=kappa_pq,
            kappa_error_recovery=kappa_er,
            kappa_goal_alignment=kappa_ga,
            kappa_overall=kappa_overall,
        )



def _fleiss_kappa(irr: IRRCalculator, matrix: list[list[int]], n_cat: int) -> float:
    """Run Fleiss' κ and extract the float result.

    ``IRRCalculator.fleiss_kappa`` returns ``dict[str, object]``; this wrapper
    casts the ``"kappa"`` value to ``float`` in a mypy-safe way.

    Args:
        irr: :class:`~src.annotation.irr_calculator.IRRCalculator` instance.
        matrix: Label matrix (n_items × n_raters), 0-indexed.
        n_cat: Number of rating categories.

    Returns:
        Fleiss' κ as a float.
    """
    raw = irr.fleiss_kappa(matrix, n_categories=n_cat)
    val = raw["kappa"]
    if not isinstance(val, (int, float)):
        raise TypeError(f"Expected numeric kappa, got {type(val)}")
    return float(val)


def _kappa_interpretation(kappa: float) -> str:
    """Return a Landis & Koch (1977) qualitative label for a kappa value.

    Args:
        kappa: Observed κ or α value.

    Returns:
        One of: ``"poor"``, ``"slight"``, ``"fair"``, ``"moderate"``,
        ``"substantial"``, ``"almost perfect"``.
    """
    if kappa < 0.0:
        return "poor"
    if kappa <= 0.20:
        return "slight"
    if kappa <= 0.40:
        return "fair"
    if kappa <= 0.60:
        return "moderate"
    if kappa <= 0.80:
        return "substantial"
    return "almost perfect"


def _sha256_seed(key: str) -> int:
    """Derive a 32-bit integer seed from a UTF-8 string via SHA-256.

    Matches the seeding convention in
    :func:`src.annotation.annotator_simulator.AnnotatorSimulator._dry_run_scores`.

    Args:
        key: Arbitrary identifier string.

    Returns:
        Little-endian 32-bit integer derived from the first 4 bytes of the
        SHA-256 digest.
    """
    digest = hashlib.sha256(key.encode()).digest()[:4]
    return int.from_bytes(digest, "little")


def _dry_run_step_score(
    pair_id: str,
    agent_id: str,
    step_index: int,
    step_type: str,
    persona_name: str,
) -> int:
    """Return a deterministic ``step_quality`` score (1–4) for dry-run Mode A.

    Standard steps are seeded by ``(pair_id, agent_id, step_index,
    persona_name)`` and sampled from the persona's ``_STANDARD_STEP_BIAS``
    range.  This produces moderate variance that would yield κ ≈ 0.45 if
    all steps were standard.

    Detour steps return ``_DETOUR_SCORES[persona_name]`` exactly — fixed
    values chosen to maximise inter-persona disagreement and drive the
    weighted-average κ toward ≈ 0.28.

    Args:
        pair_id: Zero-padded pair number.
        agent_id: ``"agent_a"`` | ``"agent_b"``.
        step_index: 0-based step position.
        step_type: ``"standard"`` | ``"detour"``.
        persona_name: One of the 5 annotator personas.

    Returns:
        Integer in [1, 4].
    """
    if step_type == "detour":
        return _DETOUR_SCORES[persona_name]
    lo, hi = _STANDARD_STEP_BIAS[persona_name]
    seed = _sha256_seed(f"{pair_id}:{agent_id}:{step_index}:{persona_name}")
    rng = random.Random(seed)
    return rng.randint(lo, hi)


def _dry_run_pia_score(
    scenario: str,
    path_style: str,
    dimension: str,
    persona_name: str,
) -> int | None:
    """Return a deterministic PIA dimension score (1–5) for dry-run Mode B.

    Scores are keyed by ``(scenario, path_style, dimension, persona_name)``
    so that the 20-item rating matrix spans the full 1–5 scale — creating
    low marginal P̄_e and enabling high Fleiss' κ ≈ 0.71.

    Returns ``None`` for the ``(direct, error_recovery)`` combination because
    direct-path agents have no recovery moments — callers must propagate
    ``None`` to :attr:`PIAAnnotation.error_recovery`.

    Args:
        scenario: One of the 5 scenario type strings.
        path_style: ``"direct"`` | ``"indirect"``.
        dimension: One of ``_PIA_DIMENSIONS``.
        persona_name: One of the 5 annotator personas.

    Returns:
        Integer in [1, 5] or ``None``.
    """
    if path_style == "direct" and dimension == "error_recovery":
        return None
    return _PIA_SCORES[scenario][path_style][dimension][persona_name]


def _build_label_matrix(
    item_ids: list[str],
    persona_order: list[str],
    score_map: dict[tuple[str, str], int],
    scale_offset: int,
) -> list[list[int]]:
    """Build a Fleiss' κ label matrix (n_items × n_raters) with 0-indexed labels.

    Args:
        item_ids: Ordered list of item identifiers (step_ids or agent keys).
        persona_order: Ordered list of rater names.
        score_map: ``(item_id, persona_name)`` → raw integer score.
        scale_offset: Subtract this from each raw score to obtain a 0-indexed
            label.  Use ``1`` for 1-based scales.

    Returns:
        2-D list of shape ``[len(item_ids) × len(persona_order)]`` where
        each cell is a 0-indexed integer category label.
    """
    matrix: list[list[int]] = []
    for item in item_ids:
        row = [score_map[(item, p)] - scale_offset for p in persona_order]
        matrix.append(row)
    return matrix



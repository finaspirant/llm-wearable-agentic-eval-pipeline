class _StandardStepAnnotator:
    """Scores every step in both agent paths using the 5 annotator personas.

    In dry-run mode uses :func:`_dry_run_step_score` — no API calls.

    Detour steps receive fixed per-persona scores from :data:`_DETOUR_SCORES`
    to ensure target standard κ ≈ 0.28.  Standard steps receive seeded scores
    from :data:`_STANDARD_STEP_BIAS`.

    Args:
        dry_run: If ``True`` (the only supported mode), use deterministic
            scoring.
    """

    def __init__(self, dry_run: bool = True) -> None:
        if not dry_run:
            raise NotImplementedError(
                "Live API mode is not implemented. Use --dry-run."
            )
        self._dry_run = dry_run

    def annotate_pair(self, pair: TrajectoryPair) -> list[StepAnnotation]:
        """Score all steps in both agents' paths for one pair.

        Returns exactly ``5 × (n_steps_a + n_steps_b)`` :class:`StepAnnotation`
        objects — one per ``(step, persona)`` combination.

        Args:
            pair: :class:`TrajectoryPair` to annotate.

        Returns:
            Flat list of :class:`StepAnnotation` objects.
        """
        results: list[StepAnnotation] = []
        for traj in (pair.agent_a, pair.agent_b):
            for step in traj.steps:
                for persona in _PERSONAS:
                    score = _dry_run_step_score(
                        pair.pair_id,
                        traj.agent_id,
                        step.step_index,
                        step.step_type,
                        persona,
                    )
                    step_id = f"{pair.pair_id}/{traj.agent_id}/{step.step_index}"
                    results.append(
                        StepAnnotation(
                            step_id=step_id,
                            pair_id=pair.pair_id,
                            agent_id=traj.agent_id,
                            step_index=step.step_index,
                            step_type=step.step_type,
                            scenario=pair.scenario,
                            persona_name=persona,
                            step_quality=score,
                            rationale=(
                                f"[DRY-RUN] {persona} on {pair.scenario} "
                                f"{step.step_type} step: sq={score}."
                            ),
                        )
                    )
        return results

    def annotate_all(self, pairs: list[TrajectoryPair]) -> list[StepAnnotation]:
        """Score all steps across all pairs.

        Args:
            pairs: List of :class:`TrajectoryPair` objects.

        Returns:
            Flat list of all :class:`StepAnnotation` objects.
        """
        out: list[StepAnnotation] = []
        for pair in pairs:
            out.extend(self.annotate_pair(pair))
        return out



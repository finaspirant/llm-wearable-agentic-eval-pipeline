def _load_pairs(pairs_dir: Path) -> list[TrajectoryPair]:
    """Load and deserialise all pair JSON files from ``pairs_dir``.

    Files must be named ``pair_01.json`` … ``pair_10.json``.

    Args:
        pairs_dir: Directory containing the pair JSON files.

    Returns:
        List of :class:`TrajectoryPair` objects in pair-number order.

    Raises:
        FileNotFoundError: If ``pairs_dir`` does not exist or any expected
            file is missing.
    """
    if not pairs_dir.exists():
        raise FileNotFoundError(
            f"pairs_dir does not exist: {pairs_dir}. "
            "Run pia_trajectory_generator first."
        )

    pairs: list[TrajectoryPair] = []
    for i in range(1, 11):
        path = pairs_dir / f"pair_{i:02d}.json"
        if not path.exists():
            raise FileNotFoundError(
                f"Expected pair file missing: {path}. "
                "Run pia_trajectory_generator first."
            )
        raw = json.loads(path.read_text())

        # Deserialise nested dataclasses from dicts.
        def _step(d: dict[str, Any]) -> PairStep:
            return PairStep(
                step_index=d["step_index"],
                step_name=d["step_name"],
                step_type=d["step_type"],
                observation=d["observation"],
                reasoning=d["reasoning"],
                action=d["action"],
                confidence=float(d["confidence"]),
            )

        def _traj(d: dict[str, Any]) -> AgentTrajectory:
            return AgentTrajectory(
                agent_id=d["agent_id"],
                path_style=d["path_style"],
                n_steps=int(d["n_steps"]),
                overall_goal_achieved=bool(d["overall_goal_achieved"]),
                session_outcome=d["session_outcome"],
                terminal_action=d["terminal_action"],
                steps=[_step(s) for s in d["steps"]],
            )

        pairs.append(
            TrajectoryPair(
                pair_id=raw["pair_id"],
                scenario=raw["scenario"],
                goal=raw["goal"],
                consent_model=raw["consent_model"],
                sensor_context=dict(raw["sensor_context"]),
                ground_truth_outcome=raw["ground_truth_outcome"],
                shared_terminal_action=raw["shared_terminal_action"],
                path_divergence_description=raw["path_divergence_description"],
                standard_kappa_prediction=raw["standard_kappa_prediction"],
                pia_rubric_prediction=raw["pia_rubric_prediction"],
                agent_a=_traj(raw["agent_a"]),
                agent_b=_traj(raw["agent_b"]),
            )
        )
    return pairs


